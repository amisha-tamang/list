# Max Element

# Code likho jo iss list mein se maximum 
# dhund kar ke print kare.Iss list ke liye aapke program ka output 
# 70 hona chaiye.Iss list ke liye aapke program ka output 70 hona chaiye.


numbers = [50, 40, 23, 70, 56, 12, 5, 10, 7] 
max=numbers[0]
i=0
while i<len(numbers):
    num=numbers[i]
    if num>max:
      max=num  
    i=i+1
print(max)            






